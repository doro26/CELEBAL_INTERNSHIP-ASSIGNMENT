
# This function checks if a given year is a leap year or not.
# A leap year is:
# - Divisible by 4, AND
# - Not divisible by 100 unless also divisible by 400.
# Returns True if leap year, otherwise False.
# Great for practicing conditionals and boolean logic in Python.

if __name__ == '__main__':
    a = int(input())
    b = int(input())
    add=a+b
    sub=a-b
    mul=a*b
    print(add)
    print(sub)
    print(mul)
